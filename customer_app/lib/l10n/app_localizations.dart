/// Tamil (ta), Telugu (te), Malayalam (ml), Urdu (ur), Arabic (ar)
class AppLocalizations {
  final Locale locale;

  AppLocalizations(this.locale);

  static AppLocalizations of(BuildContext context) {
    return Localizations.of<AppLocalizations>(context, AppLocalizations)!;
  }

  static const LocalizationsDelegate<AppLocalizations> delegate =
      _AppLocalizationsDelegate();

  static const List<Locale> supportedLocales = [
    Locale('en', ''), // English
    Locale('es', ''), // Spanish
    Locale('hi', ''), // Hindi
    Locale('ne', ''), // Nepali
    Locale('bn', ''), // Bengali
    Locale('ta', ''), // Tamil
    Locale('te', ''), // Telugu
    Locale('ml', ''), // Malayalam
    Locale('ur', ''), // Urdu
    Locale('ar', ''), // Arabic
  ];

  // Get translations based on current locale
  Map<String, String> get _localizedValues {
    switch (locale.languageCode) {
      case 'es':
        return _spanishTranslations;
      case 'hi':
        return _hindiTranslations;
      case 'ne':
        return _nepaliTranslations;
      case 'bn':
        return _bengaliTranslations;
      case 'ta':
        return _tamilTranslations;
      case 'te':
        return _teluguTranslations;
      case 'ml':
        return _malayalamTranslations;
      case 'ur':
        return _urduTranslations;
      case 'ar':
        return _arabicTranslations;
      case 'en':
      default:
        return _englishTranslations;
    }
  }

  String translate(String key) {
    return _localizedValues[key] ?? key;
